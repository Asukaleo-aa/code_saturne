c@foncb
CFONC
CFONC    SOUS-PROGRAMME DU MODULE LAGRANGIEN :
CFONC    -------------------------------------
CFONC
CFONC     SOUS-PROGRAMME UTILISATEUR (INTERVENTION NON OBLIGATOIRE)
CFONC
CFONC     MODIFICATION DU CALCUL DU TEMPS DE RELAXATION THERMIQUES
CFONC       DES PARTICULES EN FONCTION DE LA FORMULATION CHOISIE
CFONC       LE CALCUL DU NUSSELT
CFONC
CFONC
CFONC                m   Cp
CFONC                 p    p
CFONC       Tau = ---------------
CFONC          c          2
CFONC                PI d    h
CFONC                    p    e
CFONC
CFONC      Tau  : TEMPS DE RELAXATION THERMIQUE (VALEUR A CALCULER)
CFONC         c
CFONC
CFONC      m    : MASSE DE LA PARTICULE
CFONC       p
CFONC
CFONC      Cp   : CHALEUR SPECIFIQUE DE LA PARTICULE
CFONC        p
CFONC
CFONC      d    : DIAMETRE DE LA PARTICULE
CFONC       p
CFONC
CFONC      h    : COEFFICIENT D'ECHANGE THERMIQUE
CFONC       e
CFONC
CFONC     LE COEFFICIENT D'ECHANGE THERMIQUE EST CALCULE A PARTIR
CFONC       D'UN NOMBRE DE NUSSELT, LUI MEME EVALUE PAR UNE
CFONC       CORRELATION (RANZ-MARSHALL EN STANDARD).
CFONC
CFONC             h  d
CFONC              e  p
CFONC      Nu = --------  = 2 + 0.55 Re **(0.5) Prt**(0.33)
CFONC            Lambda                p
CFONC
CFONC      Lambda : CONDUCTIVITE THERMIQUE DE LA PHASE PORTEUSE
CFONC
CFONC      Re     : NOMBRE DE REYNOLDS PARTICULAIRES
CFONC        p
CFONC
CFONC      Prt    : NOMBRE DE PRANDTL
CFONC
CFONC
CFONC     CE SOUS PROGRAMME EST APPELE DANS UNE BOUCLE SUR
CFONC       LES PARTICULES : ATTENTION DE NE PAS TROP LE CHARGER
CFONC
