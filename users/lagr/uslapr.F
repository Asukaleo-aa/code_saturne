CFONC     ROUTINE UTILISATEUR POUR LES CONDITIONS AUX LIMITES RELATIVES
CFONC       AUX PARTICULES (ENTREE ET TRAITEMENT AUX AUTRES BORDS)
CFONC          PERMET D'IMPOSER LES VALEURS DE
CFONC            - VITESSE
CFONC            - DIAMETRE
CFONC            - TEMPERATURE
CFONC          POUR LA PARTICULE TRAITER
CFONC     SI IDVAR = 0 ===> ON RECUPERE LE TAUX DE PRESENCE
CFONC     SI IDVAR = 1 ===> ON RECUPERE LES 3 COMPOSANTES DE LA VITESSE
CFONC     SI IDVAR = 2 ===> ON RECUPERE LE DIAMETRE
CFONC     SI IDVAR = 3 ===> ON RECUPERE LA TEMPERATURE
CFONC
CFONC
